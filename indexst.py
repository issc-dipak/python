# Define a string
text = "BananaChips"

# Indexing (positive)
print("Character at index 0:", text[0])    # First character
print("Character at index 3:", text[3])    # Fourth character

# Indexing (negative)
print("Last character (index -1):", text[-1])
print("Second last character (index -2):", text[-2])

# Slicing a part of the string
print("First 6 characters:", text[:6])
print("From index 6 to end:", text[6:])
print("Characters from index 2 to 5:", text[2:6])
