# Array using list
arr = [10, 20, 30, 40, 50]

# Access elements
print("First element:", arr[0])
print("Last element:", arr[-1])

# Update element
arr[2] = 35
print("Updated array:", arr)

# Append element
arr.append(60)
print("After append:", arr)

# Remove element
arr.remove(20)
print("After removing 20:", arr)

# Length of array
print("Length:", len(arr))
